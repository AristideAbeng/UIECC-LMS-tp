     private void miseAjour(){
       
         String typeUsager = this.cbbx_SelectionTypeUsager_recuperer.getSelectedItem().toString();
        String identifiantUsager = this.txt_identifiant_usager_recuperer.getText();
        if(typeUsager.equals("Etudiant")){
            boolean validation = Etudiant.validerMatricule(identifiantUsager);
            if(validation){
                this.mUtilisateur_retourner = Etudiant.getEtudiant(identifiantUsager);
                this.typeUsager_retourner = typeUsager;
                
               
                
                JList<Emprunt> emprunts = Etudiant.chercherEmpruntsEtudiant(identifiantUsager);
                 emprunts.addListSelectionListener(this.createEmpruntSelectionListener(emprunts));
                if(emprunts.getModel().getSize() == 0){
                    System.out.println("no pending borrows");
                    this.panelEmprunts_NonRembourses_recuperer.removeAll();
                    this.panelEmprunts_NonRembourses_recuperer.add(new JLabel("          Aucun Emprunt non Rembourse"),BorderLayout.CENTER); 
                     this.panelEmprunts_NonRembourses_recuperer.setVisible(true);
                     this.panelEmprunts_NonRembourses_recuperer.revalidate();
                     this.panelEmprunts_NonRembourses_recuperer.repaint();
                     this.panelEmprunts_NonRembourses_recuperer.updateUI();
                  
                      
                }else{
                    System.out.println("Emprunts trouves");
                     this.panelEmprunts_NonRembourses_recuperer.removeAll();
                    this.panelEmprunts_NonRembourses_recuperer.add(new JScrollPane(emprunts),BorderLayout.CENTER);
                      this.panelEmprunts_NonRembourses_recuperer.setVisible(true);
                     this.panelEmprunts_NonRembourses_recuperer.revalidate();
                     this.panelEmprunts_NonRembourses_recuperer.repaint();
                     this.panelEmprunts_NonRembourses_recuperer.updateUI();
                }
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Identifiant INVALIDE");
            }
            
        }
    }
   

