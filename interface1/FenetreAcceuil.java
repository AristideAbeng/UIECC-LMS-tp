    private void jLabel30MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel30MouseClicked
        // TODO add your handling code here:
          String typeUsager = this.cbbx_SelectionTypeUsager_transaction.getSelectedItem().toString();
        String identifiantUsager = this.txt_identifiant_usager_transaction.getText();
        if(typeUsager.equals("Etudiant")){
            boolean validation = Etudiant.validerMatricule(identifiantUsager);
            if(validation){
                this.mUtilisateur_emprunt = Etudiant.getEtudiant(identifiantUsager);
                this.typeUsager_emprunt = typeUsager;
                 this.panel_Emprunts_NonRembourses_transaction.removeAll();
               
                JOptionPane.showMessageDialog(rootPane, "Identifiant valide");
                JList<Emprunt> emprunts = Etudiant.chercherEmpruntsEtudiant(identifiantUsager);
                if(emprunts.getModel().getSize() == 0){
                    System.out.println("no pending borrows");
                    this.panel_Emprunts_NonRembourses_transaction.add(new JLabel("          Aucun Emprunt non Rembourse"),BorderLayout.CENTER); 
                     this.panel_Emprunts_NonRembourses_transaction.setVisible(true);
                     this.panel_Emprunts_NonRembourses_transaction.updateUI();
                         
                    this.lbl_choisir_emprunt.setEnabled(true);
                    this.lbl_enregistrer_emprunt.setEnabled(false);
                      
                }else{
                    System.out.println("Emprunts trouves");
                    Etudiant etudiant = Etudiant.getEtudiant(identifiantUsager);
                    if(Etudiant.validerPossibiliterNouvelEmpruntAvantSelection(etudiant)){
                       this.lbl_choisir_emprunt.setEnabled(true);
                       this.lbl_enregistrer_emprunt.setEnabled(false);
                    }else{
                        this.lbl_emprunt_interdit_transaction.setVisible(true);
                        this.lbl_choisir_emprunt.setEnabled(false);
                        this.lbl_enregistrer_emprunt.setEnabled(false);
                    }
                    
                    this.panel_Emprunts_NonRembourses_transaction.add(new JScrollPane(emprunts),BorderLayout.CENTER);
                     this.panel_Emprunts_NonRembourses_transaction.setVisible(true);
                     this.panel_Emprunts_NonRembourses_transaction.updateUI();
                    
                }
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Identifiant INVALIDE");
            }
            
        }
    }//GEN-LAST:event_jLabel30MouseClicked
