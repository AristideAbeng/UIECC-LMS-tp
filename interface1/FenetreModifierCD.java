    private void btn_mettreAJourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mettreAJourActionPerformed
        // TODO add your handling code here:
         int option = JOptionPane.showConfirmDialog(null,"Voulez vous vraiment mettre ce livre a jour ?");
        if(option==0){
            this.mCD.auteur = this.lbl_auteur.getText();
            this.mCD.datePublication = this.txt_date_publication.getDate();
            this.mCD.taille = Integer.parseInt(this.lbl_taille.getText());
            String duree = this.lbl_duree.getText();
            this.mCD.maisonEdition = this.lbl_maison_edition.getText();
            this.mCD.nbExemplaires = Integer.parseInt(this.lbl_nb_Exemplaire.getText());
            this.mCD.positionement = this.lbl_positionement.getText();
            this.mCD.titre = this.lbl_titre.getText();
            this.mCD.nomImage = this.lbl_image.getText();
            
                
        try{
              java.util.Date d = this.mCD.datePublication;
           java.sql.Date sqldate = null;
            if (d == null) {
                System.out.println("No date specified!");
            } else {
                sqldate = new java.sql.Date(d.getTime());
                // Do something with sqldate
            }
            
                  Connection con = DBConnect.connect();
                 String insertQuery = "UPDATE cd SET Titre = ?,"
                         + " Auteur = ?,"
                         + " DatePublication = ?,"
                         + " NombreDexemplaires = ?,"
                         + " Positionement = ?,"
                         + "MaisonEdition = ?,"
                         + " duree = ?,"
                         + " taille = ?,"
                         + "nomImage = ?"
                         + "WHERE Id = ?";
                 
               PreparedStatement requeteInsertion = con.prepareStatement(insertQuery);
                requeteInsertion.setString(1,this.mCD.titre);
                 requeteInsertion.setString(2,this.mCD.auteur);
                  requeteInsertion.setDate(3, sqldate);
                  requeteInsertion.setInt(4, this.mCD.nbExemplaires);
                   requeteInsertion.setString(5,this.mCD.positionement);
                    requeteInsertion.setString(6,this.mCD.maisonEdition);
                     requeteInsertion.setString(7,duree);
                      requeteInsertion.setInt(8,this.mCD.taille);
                     
                      requeteInsertion.setString(9,this.mCD.nomImage);
                      requeteInsertion.setInt(10, this.mCD.id);
                      
                requeteInsertion.execute();
                JOptionPane.showMessageDialog(null, "Enregistree avec success");
            
                con.close();
        }catch(Exception e){
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_btn_mettreAJourActionPerformed

