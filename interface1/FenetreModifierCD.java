    private void btn_changerImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changerImageActionPerformed
        // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser();
        fileChooser.addChoosableFileFilter(new ImageFilter());
        fileChooser.setAcceptAllFileFilterUsed(false);
        
        int option = fileChooser.showOpenDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            this.lbl_image.setText(file.getName());
            
          
                 BufferedImage img = null;
            try {
                img = ImageIO.read(file);
                 ImageIO.write(img,"jpg",new File(PROJECTCONSTANTS.IMAGES_PATH+file.getName()));
                Image dimg = img.getScaledInstance(this.lbl_image.getWidth(),this.lbl_image.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(dimg);
                this.lbl_image.setIcon(imageIcon);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }else{
             this.lbl_image.setText(" Aucune Image Choisie  ");
        }
    }//GEN-LAST:event_btn_changerImageActionPerformed

