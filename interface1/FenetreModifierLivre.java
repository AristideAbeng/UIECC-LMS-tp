    private void btn_MettreLivreAJourMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_MettreLivreAJourMouseClicked
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null,"Voulez vous vraiment mettre ce livre a jour ?");
        if(option==0){
            this.mLivre.auteur = this.lbl_auteur_livre.getText();
            this.mLivre.categorie = this.lbl_categorie.getText();
            this.mLivre.datePublication = this.datechooser_ModifierDatePublication.getDate();
            this.mLivre.edition = this.lbl_edition.getText();
            this.mLivre.maisonEdition = this.lbl_maison_edition.getText();
            this.mLivre.nbExemplaires = Integer.parseInt(this.lbl_nb_exemplaire.getText());
            this.mLivre.positionement = this.lbl_positionement.getText();
            this.mLivre.titre = this.lbl_titre_livre.getText();
            this.mLivre.nomImage = this.newImageName;
            
                
        try{
              java.util.Date d = this.mLivre.datePublication;
           java.sql.Date sqldate = null;
            if (d == null) {
                System.out.println("No date specified!");
            } else {
                sqldate = new java.sql.Date(d.getTime());
                // Do something with sqldate
            }
            
                  Connection con = DBConnect.connect();
                 String insertQuery = "UPDATE livre SET Titre = ?,"
                         + " Auteur = ?,"
                         + " DatePublication = ?,"
                         + " NombreDexemplaires = ?,"
                         + " Positionement = ?,"
                         + "MaisonEdition = ?,"
                         + " Stylecategorie = ?,"
                         + " nbPage = ?,"
                         + " edition = ?,"
                         + "nomImage = ?"
                         + "WHERE Id = ?";
                 
               PreparedStatement requeteInsertion = con.prepareStatement(insertQuery);
                requeteInsertion.setString(1,this.mLivre.titre);
                 requeteInsertion.setString(2,this.mLivre.auteur);
                  requeteInsertion.setDate(3, sqldate);
                  requeteInsertion.setInt(4, this.mLivre.nbExemplaires);
                   requeteInsertion.setString(5,this.mLivre.positionement);
                    requeteInsertion.setString(6,this.mLivre.maisonEdition);
                     requeteInsertion.setString(7,this.mLivre.categorie);
                      requeteInsertion.setInt(8,this.mLivre.nbPages);
                      requeteInsertion.setString(9,this.mLivre.edition);
                      requeteInsertion.setString(10,this.mLivre.nomImage);
                      requeteInsertion.setInt(11, this.mLivre.id);
                      
                requeteInsertion.execute();
                JOptionPane.showMessageDialog(null, "Enregistree avec success");
            
                con.close();
        }catch(Exception e){
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_btn_MettreLivreAJourMouseClicked

