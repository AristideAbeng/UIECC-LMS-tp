    private void btn_changer_imageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_changer_imageMouseClicked
        // TODO add your handling code here:
        
         JFileChooser fileChooser = new JFileChooser();
        fileChooser.addChoosableFileFilter(new ImageFilter());
        fileChooser.setAcceptAllFileFilterUsed(false);
        
        int option = fileChooser.showOpenDialog(this);
        if(option == JFileChooser.APPROVE_OPTION){
            File file = fileChooser.getSelectedFile();
            
            
            try{
                this.newImageName = file.getName();
                RenderedImage image = ImageIO.read(file);
                ImageIO.write(image,"jpg",new File(path+file.getName()));
                this.lbl_image.setIcon(new ImageIcon(path+file.getName()));
            }catch(IOException e){
                System.out.println(e);
            }
        }else{
             this.lbl_image.setText(" Aucune Image Choisie  ");
        }
        
        
        
    }//GEN-LAST:event_btn_changer_imageMouseClicked

